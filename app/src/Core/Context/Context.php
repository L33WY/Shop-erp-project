<?php declare(strict_types=1);

namespace App\Core\Context;

use App\Entity\User;

class Context
{
    private User $user;

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }

    /**
     * @param User $user
     */
    public function setUser(User $user): void
    {
        $this->user = $user;
    }

}